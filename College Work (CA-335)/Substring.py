# Program to create, concatenate, print strings and access substring

# Creating strings
str1 = "Hello"
str2 = "Python"

# Concatenating strings
result = str1 + " " + str2

# Printing the concatenated string
print("Concatenated String:", result)

# Accessing substring using slicing
# (start_index : end_index)
substring1 = result[0:5]     # "Hello"
substring2 = result[6:12]    # "Python"

# Printing substrings
print("Substring (0-5):", substring1)
print("Substring (6-12):", substring2)