# Single-line strings
single = 'Hello'
double = "World"

# Multi-line strings
multi = '''This is
a multi-line
string'''

# Raw strings (ignores escape characters)
raw = r"Hello\nWorld"  # prints: Hello\nWorld

# f-strings (formatted string literals)
name = "Alice"
fstring = f"Hello, {name}!"

# Byte strings
byte_str = b"Hello"
