# Single-line strings
single = 'Hello'
double = "Ranger"

# Multi-line strings
multi = '''This is
a multi-line
string'''

# Raw strings (ignores escape characters)
raw = r"Hello\nRanger"  # prints: Hello\nRanger

# f-strings (formatted string literals)
name = "Chandan"
fstring = f"Hello, {name}!"  # prints: Hello, Chandan!

# Byte strings
byte_str = b"Hello"  # prints: b'Hello'
